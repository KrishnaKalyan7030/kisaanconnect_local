// ─── Language Engine ──────────────────────────────────────────────────────────

const SUPPORTED_LANGS = ["en", "hi", "mr", "pa"];
const LANG_NAMES = { en: "English", hi: "हिंदी", mr: "मराठी", pa: "ਪੰਜਾਬੀ" };

function t(key) {
  const lang = window.__currentLang || "en";
  return (translations[lang] && translations[lang][key]) || translations["en"][key] || key;
}

function applyTranslations() {
  const lang = window.__currentLang || "en";
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const val = (translations[lang] && translations[lang][key]) || translations["en"][key];
    if (!val) return;
    if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });
  // Update lang switcher button label
  document.querySelectorAll(".lang-current").forEach(el => {
    el.textContent = LANG_NAMES[lang] || lang.toUpperCase();
  });
}

function setLanguage(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  window.__currentLang = lang;
  localStorage.setItem("kc_lang", lang);
  applyTranslations();
  // Close dropdown if open
  document.querySelectorAll(".lang-dropdown").forEach(d => d.classList.add("hidden"));
}

function initLanguage() {
  const saved = localStorage.getItem("kc_lang") || "en";
  window.__currentLang = saved;
  applyTranslations();
}

// ─── Language Switcher HTML builder ──────────────────────────────────────────
// Call this to get the switcher HTML to inject into navbars
